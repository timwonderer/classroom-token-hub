class AnalyticsAlert(db.Model):
    """
    Stores analytics alerts generated by the analytics engine.

    Alerts are window-scoped, deterministic, and lifecycle-aware.
    They are not notifications; they are evaluative signals tied to a specific snapshot.
    """
    __tablename__ = 'analytics_alerts'

    id = db.Column(db.Integer, primary_key=True)

    # Deterministic identity (prevents duplicate / zombie alerts)
    alert_key = db.Column(db.String(100), nullable=False)  # e.g. 'participation_low'
    class_id = db.Column(db.String(36), db.ForeignKey('classes.class_id', ondelete='CASCADE'), nullable=True, index=True)
    join_code = db.Column(db.String(20), nullable=False, index=True)

    # Snapshot window this alert applies to
    window_type = db.Column(db.String(10), nullable=False)  # 'week', 'month', 'pay_cycle', 'rent_cycle' (see analytics.ALLOWED_WINDOW_TYPES)
    window_start = db.Column(db.DateTime(timezone=True), nullable=False)
    window_end = db.Column(db.DateTime(timezone=True), nullable=False)

    # Severity & messaging
    severity = db.Column(db.String(20), nullable=False)  # 'info', 'warning', 'critical'
    what_changed = db.Column(db.Text, nullable=False)
    why_it_matters = db.Column(db.Text, nullable=False)
    suggested_action = db.Column(db.Text, nullable=True)

    # Lifecycle timestamps
    created_at = db.Column(db.DateTime(timezone=True), default=utc_now, nullable=False)
    acknowledged_at = db.Column(db.DateTime(timezone=True), nullable=True)
    resolved_at = db.Column(db.DateTime(timezone=True), nullable=True)

    # Optional automatic expiration (for time-bound alerts)
    valid_until = db.Column(db.DateTime(timezone=True), nullable=True)

    __table_args__ = (
        # Enforce one active alert per type / class / window
        db.UniqueConstraint(
            'alert_key',
            'join_code',
            'window_type',
            'window_start',
            'window_end',
            name='uq_analytics_alert_identity'
        ),
        db.Index('ix_analytics_alerts_join_code_active', 'join_code', 'resolved_at'),
        db.Index('ix_analytics_alerts_window', 'window_type', 'window_start', 'window_end'),
    )

    @hybrid_property
    def is_active(self):
        """Return True if alert is unresolved and within its validity window."""
        if self.resolved_at is not None:
            return False
        if self.valid_until is not None and utc_now() > ensure_utc(self.valid_until):
            return False
        return True

    @is_active.expression
    def is_active(self):
        return sa.and_(
            self.resolved_at.is_(None),
            sa.or_(self.valid_until.is_(None), self.valid_until > sa.func.now())
        )

    def acknowledge(self):
        """Mark alert as acknowledged by a human."""
        if self.acknowledged_at is None:
            self.acknowledged_at = utc_now()

    def resolve(self):
        """Mark alert as resolved by the system."""
        if self.resolved_at is None:
            self.resolved_at = utc_now()

    def __repr__(self):
        return (
            f'<AnalyticsAlert {self.alert_key} '
            f'[{self.severity}] '
            f'join={self.join_code} '
            f'window={self.window_type} '
            f'{self.window_start.date()}–{self.window_end.date()}>'
        )


